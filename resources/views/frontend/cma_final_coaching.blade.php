                Final
                application form 2024
                by selecting
                the &quot;Admission&quot; option.</p>

            <p class="content-text"><b>Step:3</b> On the admissions page, click on the &quot;Online admission&quot;
                option
                in red font on the
                left side of the
                page.</p>

            <p class="content-text"><b>Step:4</b> To continue, click the link &quot;Apply for Final Course&quot; and
                fill
